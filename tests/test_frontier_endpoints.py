# NEGATIVE contract: unknown-id / bad-params -> HONEST 4xx, NEVER a 500.
# ---------------------------------------------------------------------------

def _bad_variants(entry):
    """Yield (label, response) for degenerate inputs against this endpoint."""
    variants = []
    if entry["method"] == "POST":
        neg = entry.get("neg_body")
        if neg is not None:
            variants.append(("neg_body", _request(entry, override_body=neg)))
        # a structurally-wrong body (list instead of object)
        variants.append(("wrong-type-body", client.post(entry["path"], json=[1, 2, 3])))
    else:
        # inject an obviously-bad param value
        base_q = entry.get("query", "")
        if base_q:
            # corrupt the first key's value with a non-numeric junk token
            first = base_q.split("&")[0].split("=")[0]
            bad_q = f"{first}=__not_a_valid_value__&" + base_q
        else:
            bad_q = "seed=__not_a_valid_value__&distance=-999999&n=not_an_int"
        variants.append(("bad-params", _request(entry, override_query=bad_q)))
    return variants


@pytest.mark.parametrize("entry", MANIFEST, ids=IDS)
def test_frontier_endpoint_bad_input_never_500(entry):
    """Malformed/unknown input must degrade HONESTLY (<500), never crash with a 500."""
    for label, r in _bad_variants(entry):
        if (
            label == "wrong-type-body"
            and any(s in entry["path"] for s in SUBSTRATE_KNOWN_500_ON_LIST_BODY)
            and r.status_code == 500
        ):
            pytest.xfail(
                f"{entry['path']} wrong-type body 500 lives in szl-substrate "
                f"(szl_restraint._evaluate) — substrate-repo fix, not a11oy/killinchu. "
                f"Invariant still enforced for every other endpoint."
            )
        # A 500 is a hard crash — always forbidden. An honest, labelled 503 UNAVAILABLE
        # is an acceptable degrade (e.g. optional data file absent), not a crash.
        assert r.status_code != 500, (
            f"{entry['path']} [{label}] returned 500 on bad input — an unhandled crash "
            f"on malformed input is exactly the silent-degrade class this gate forbids. "
            f"Body: {r.text[:300]}"
        )
        if r.status_code >= 500:
            # Only an HONEST 503 UNAVAILABLE is tolerated among 5xx.
            assert r.status_code == 503 and any(
                w in r.text.lower() for w in ("unavailable", "honest", "label", "note")
            ), (
                f"{entry['path']} [{label}] returned {r.status_code} — only an honest "
                f"labelled 503 is tolerated. Body: {r.text[:200]}"
            )


@pytest.mark.parametrize(
    "entry",
    [e for e in MANIFEST if "neg_body" in e],
    ids=[f"{e['method']} {e['path']}" for e in MANIFEST if "neg_body" in e],
)
def test_frontier_endpoint_unknown_id_is_4xx(entry):
    """Endpoints with a well-defined 'unknown id' contract must answer 4xx (not 200/500)."""
    r = _request(entry, override_body=entry["neg_body"])
    assert 400 <= r.status_code < 500, (
        f"{entry['path']} unknown-id/malformed body should be an honest 4xx, "
        f"got {r.status_code}: {r.text[:200]}"
    )


def test_manifest_is_nonempty_and_covers_core_organs():
    """Meta-guard: the manifest must be populated and cover the named core organs."""
    assert len(MANIFEST) >= 40, f"frontier manifest suspiciously small: {len(MANIFEST)}"
    paths = " ".join(e["path"] for e in MANIFEST)
    # At least this many of the brief's named organs must be represented in THIS app.
    present = [org for org in CORE_FRONTIER if org in paths]
    assert len(present) >= 3, (
        f"manifest does not cover enough named core frontier organs: {present}"
    )
